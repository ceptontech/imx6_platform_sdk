    HW_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_WR(
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_LVE(LVE_DISABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_HYS(HYS_ENABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_PUS(PUS_100KOHM_PD) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_PUE(PUE_KEEP) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_PKE(PKE_ENABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_ODE(ODE_DISABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_SPEED(SPD_100MHZ) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_DSE(DSE_40OHM) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_RST_SRE(SRE_SLOW));

    // Config usdhc2.WP to pad SD2_DAT6(Y21)
    // HW_IOMUXC_SW_MUX_CTL_PAD_SD2_DAT6_WR(0x00000004);
    // HW_IOMUXC_USDHC2_IPP_WP_ON_SELECT_INPUT_WR(0x00000002);
    // HW_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_WR(0x000170B0);
    // Mux Register:
    // IOMUXC_SW_MUX_CTL_PAD_SD2_DAT6(0x020E0274)
    //   SION [4] - Software Input On Field Reset: SION_DISABLED
    //              Force the selected mux mode Input path no matter of MUX_MODE functionality.
    //     SION_DISABLED (0) - Input Path is determined by functionality of the selected mux mode (regular).
    //     SION_ENABLED (1) - Force input path of pad SD2_DAT6.
    //   MUX_MODE [2:0] - MUX Mode Select Field Reset: ALT5
    //                    Select 1 of 8 iomux modes to be used for pad: SD2_DAT6.
    //     ALT0 (0) - Select mux mode: ALT0 mux port: DAT6 of instance: usdhc2.
    //     ALT1 (1) - Select mux mode: ALT1 mux port: DAT6 of instance: usdhc3.
    //                NOTE: - Config Register IOMUXC_USDHC3_IPP_DAT6_IN_SELECT_INPUT for mode ALT1.
    //     ALT2 (2) - Select mux mode: ALT2 mux port: RTS of instance: uart2.
    //                NOTE: - Config Register IOMUXC_UART2_IPP_UART_RTS_B_SELECT_INPUT for mode ALT2.
    //     ALT3 (3) - Select mux mode: ALT3 mux port: D[8] of instance: csi.
    //                NOTE: - Config Register IOMUXC_CSI_IPP_CSI_D_8_SELECT_INPUT for mode ALT3.
    //     ALT4 (4) - Select mux mode: ALT4 mux port: WP of instance: usdhc2.
    //                NOTE: - Config Register IOMUXC_USDHC2_IPP_WP_ON_SELECT_INPUT for mode ALT4.
    //     ALT5 (5) - Select mux mode: ALT5 mux port: GPIO[29] of instance: gpio4.
    HW_IOMUXC_SW_MUX_CTL_PAD_SD2_DAT6_WR(
            BF_IOMUXC_SW_MUX_CTL_PAD_SD2_DAT6_SION(SION_DISABLED) | 
            BF_IOMUXC_SW_MUX_CTL_PAD_SD2_DAT6_MUX_MODE(ALT4));
    // Pad SD2_DAT6 is involved in Daisy Chain.
    // Input Select Register:
    // IOMUXC_USDHC2_IPP_WP_ON_SELECT_INPUT(0x020E0834)
    //   DAISY [1:0] Reset: SEL_ECSPI1_MISO_ALT4
    //                 Selecting Pads Involved in Daisy Chain.
    //                 NOTE: Instance: usdhc2,   In Pin: ipp_wp_on
    //     SEL_ECSPI1_MISO_ALT4 (0) - Selecting Pad: ECSPI1_MISO for Mode: ALT4.
    //     SEL_EPDC_GDRL_ALT6 (1) - Selecting Pad: EPDC_GDRL for Mode: ALT6.
    //     SEL_SD2_DAT6_ALT4 (2) - Selecting Pad: SD2_DAT6 for Mode: ALT4.
    HW_IOMUXC_USDHC2_IPP_WP_ON_SELECT_INPUT_WR(
            BF_IOMUXC_USDHC2_IPP_WP_ON_SELECT_INPUT_DAISY(SEL_SD2_DAT6_ALT4));
    // Pad Control Register:
    // IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6(0x020E057C)
    //   LVE [22] - Low Voltage Enable Field Reset: LVE_DISABLED
    //              Select one out of next values for pad: SD2_DAT6.
    //     LVE_DISABLED (0) - High Voltage
    //     LVE_ENABLED (1) - Low Voltage
    //   HYS [16] - Hysteresis Enable Field Reset: HYS_ENABLED
    //              Select one out of next values for pad: SD2_DAT6.
    //     HYS_DISABLED (0) - Hysteresis Disabled
    //     HYS_ENABLED (1) - Hysteresis Enabled
    //   PUS [15:14] - Pull Up / Down Configure Field Reset: PUS_100KOHM_PD
    //                 Select one out of next values for pad: SD2_DAT6.
    //     PUS_100KOHM_PD (0) - 100K Ohm Pull Down
    //     PUS_47KOHM_PU (1) - 47K Ohm Pull Up
    //     PUS_100KOHM_PU (2) - 100K Ohm Pull Up
    //     PUS_22KOHM_PU (3) - 22K Ohm Pull Up
    //   PUE [13] - Pull / Keep Select Field Reset: PUE_KEEP
    //              Select one out of next values for pad: SD2_DAT6.
    //     PUE_KEEP (0) - Keeper
    //     PUE_PULL (1) - Pull
    //   PKE [12] - Pull / Keep Enable Field Reset: PKE_ENABLED
    //              Select one out of next values for pad: SD2_DAT6.
    //     PKE_DISABLED (0) - Pull/Keeper Disabled
    //     PKE_ENABLED (1) - Pull/Keeper Enabled
    //   ODE [11] - Open Drain Enable Field Reset: ODE_DISABLED
    //              Select one out of next values for pad: SD2_DAT6.
    //     ODE_DISABLED (0) - Open Drain Disabled
    //     ODE_ENABLED (1) - Open Drain Enabled
    //   SPEED [7:6] - Speed Field Reset: SPD_100MHZ
    //                 Select one out of next values for pad: SD2_DAT6.
    //     SPD_TBD (0) - TBD
    //     SPD_50MHZ (1) - Low(50 MHz)
    //     SPD_100MHZ (2) - Medium(100 MHz)
    //     SPD_200MHZ (3) - Maximum(200 MHz)
    //   DSE [5:3] - Drive Strength Field Reset: DSE_40OHM
    //               Select one out of next values for pad: SD2_DAT6.
    //     DSE_DISABLED (0) - Output driver disabled.
    //     DSE_240OHM (1) - 240 Ohm
    //     DSE_120OHM (2) - 120 Ohm
    //     DSE_80OHM (3) - 80 Ohm
    //     DSE_60OHM (4) - 60 Ohm
    //     DSE_48OHM (5) - 48 Ohm
    //     DSE_40OHM (6) - 40 Ohm
    //     DSE_34OHM (7) - 34 Ohm
    //   SRE [0] - Slew Rate Field Reset: SRE_SLOW
    //             Select one out of next values for pad: SD2_DAT6.
    //     SRE_SLOW (0) - Slow Slew Rate
    //     SRE_FAST (1) - Fast Slew Rate
    HW_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_WR(
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_LVE(LVE_DISABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_HYS(HYS_ENABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_PUS(PUS_47KOHM_PU) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_PUE(PUE_PULL) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_PKE(PKE_ENABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_ODE(ODE_DISABLED) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_SPEED(SPD_100MHZ) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_DSE(DSE_40OHM) | 
            BF_IOMUXC_SW_PAD_CTL_PAD_SD2_DAT6_SRE(SRE_SLOW));
