    // Pad Control Register:
    // IOMUXC_SW_PAD_CTL_PAD_SD3_DAT1(0x020E0700)
    //   HYS (16) - Hysteresis Enable Field Reset: HYS_ENABLED
    //              Select one out of next values for pad: SD3_DAT1.
